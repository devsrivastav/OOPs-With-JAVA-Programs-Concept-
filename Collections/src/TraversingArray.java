import java.util.*;
public class TraversingArray {

	public static void main(String[] args) {
		ArrayList<String> fruits = new ArrayList<String>();
		fruits.add("berry");
		fruits.add("Apple");
		fruits.add("Pineapple");
		fruits.add("Watermelon");
		for(String element : fruits) {
			System.out.println(element);
		}

	}

}
